public class Books {
    private String title;
    private int year;
    private Author author;

    public Books (String title, int year, Author author) {
        this.title = title;
        this.year = year;
        this.author = author;
    }

    public String getTitle() {
        return title;
    }
    public int getYear() {
        return year;
    }
    public Author getAuthor() {
        return author;
    }




}




